// Если проводить аналогию, то это похоже на почтовую службу:
// 1. Action (Экшен) — это «Письмо»
// Это объект, который содержит инструкцию и данные. Он сам по себе ничего не делает, он просто описывает, что произошло.
// Пример: { type: "ADD_USER", payload: { name: "Иван", job: "Повар" } }
// Здесь type — это заголовок письма («Добавить пользователя»), а payload — это само содержимое (данные из твоей формы).
// 2. Dispatch (Диспатч) — это «Почтовый ящик» или «Курьер»
// Это функция, которой ты отдаешь свой экшен. Без диспатча твой экшен — просто текст на бумаге. Когда ты вызываешь dispatch(action), ты как бы нажимаешь кнопку «Отправить».
// Твоя правда: Диспатч именно передает данные (экшен) в главный «центр управления» (который называется Редьюсер).
// 3. SetState (Action в useState)
// Когда ты используешь простой useState, там нет слова «dispatch», но функция setUser (или любая вторая переменная из массива) работает похожим образом.
// Ты вызываешь setUser(новые_данные).
// В этот момент React берет эти данные и «пропихивает» их в состояние компонента.
